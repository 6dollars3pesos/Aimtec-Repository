        /// <param name="sender">The sender.</param>
        /// <param name="args">The <see cref="PostAttackEventArgs" /> instance containing the event data.</param>
        public static void OnPostAttack(object sender, PostAttackEventArgs args)
        {
            /// <summary>
            ///     Initializes the orbwalkingmodes.
            /// </summary>
            switch (Orbwalker.Implementation.Mode)
            {
                case OrbwalkingMode.Combo:
                    Weaving(sender, args);
                    break;
                case OrbwalkingMode.Laneclear:
                    Jungleclear(sender, args);
                    break;
            }
        }

        /// <summary>
        ///     Fired on spell cast.
        /// </summary>
        /// <param name="sender">The sender.</param>
        /// <param name="args">The <see cref="SpellBookCastSpellEventArgs" /> instance containing the event data.</param>
        public static void OnCastSpell(Obj_AI_Base sender, SpellBookCastSpellEventArgs args)
        {
            if (sender.IsMe)
            {
                switch (args.Slot)
                {
                    case SpellSlot.W:
                        if (ObjectManager.Get<Obj_AI_Minion>().Any(m => m.Distance(args.End) < 200 && m.UnitSkinName.Equals("CaitlynTrap")))
                        {
                            args.Process = false;
                        }
                        break;

                    case SpellSlot.E:
                        if (Game.TickCount - UtilityClass.LastTick < 1000)
                        {
                            return;
                        }

                        if (Orbwalker.Implementation.Mode == OrbwalkingMode.None &&
                            MenuClass.Miscellaneous["reversede"].As<MenuBool>().Enabled)
                        {
                            UtilityClass.LastTick = Game.TickCount;
                            SpellClass.E.Cast(UtilityClass.Player.Position.Extend(Game.CursorPos, -SpellClass.E.Range));
                        }
                        break;
                }
            }
        }

        /// <summary>
        ///     Called on do-cast.
        /// </summary>
        /// <param name="sender">The sender.</param>
        /// <param name="args">The <see cref="Obj_AI_BaseMissileClientDataEventArgs" /> instance containing the event data.</param>
        public static void OnProcessSpellCast(Obj_AI_Base sender, Obj_AI_BaseMissileClientDataEventArgs args)
        {
            if (sender.IsMe)
            {
                /// <summary>
                ///     Initializes the orbwalkingmodes.
                /// </summary>
                switch (Orbwalker.Implementation.Mode)
                {
                    case OrbwalkingMode.Combo:
                        switch (args.SpellData.Name)
                        {
                            case "CaitlynEntrapment":
                            case "CaitlynEntrapmentMissile":
                                if (SpellClass.W.Ready &&
                                    MenuClass.Spells["w"]["triplecombo"].As<MenuBool>().Enabled)
                                {
                                    var bestTarget = GameObjects.EnemyHeroes.Where(t => t.IsValidTarget(SpellClass.E.Range)).OrderBy(o => o.Distance(args.End)).First();
                                    if (bestTarget != null)
                                    {
                                        SpellClass.W.Cast(bestTarget.Position);
                                    }
                                }
                                break;
                        }

                        break;
                }
            }
        }

